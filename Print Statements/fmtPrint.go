package main

import "fmt"

func main() {
	// Println
	name := "Raju"
	_ = name
	roll := 3
	fmt.Println("My roll number is", roll)
	fmt.Println("My name is", name)

	// Type Modifier
	// %d - Integer
	// %s - String
	// %f - Float

	// \n - New Line
	// Printf
	fmt.Printf("My Roll number is %d \n", roll)
	fmt.Printf("My name is %s", name)
}
